public class Polimorfismo {
    /*
     * Polimorfismo permite que variáveis de um mesmo tipo mais genérico possam apontar para 
     * objetos de tipos específicos
     * 
     * 
     * Account x = new Account(1020, "Alex", 1000.0);
     * Account y = new SavingAccount(1023, "Maria", 1000.0, 0.01);
     * 
     * Na memória, são objetos de tipos diferentes, mas as variáveis são do tipo Account.
     * Comportamento polimórfico; diferentes.
     * 
     * x.withdraw(50.0);
     * y.withdraw(50.0);
     * 
     * O compilador não sabe qual método será chamado, pois depende do tipo do objeto. Isso é feito
     * em tempo de execução.
     */
    
}

public class Herança {
    /*
     A herança é um tipo de associação que permite que uma classe herde todos os dados e comportamentos 
     de outra.

     A suas vantagens são o reuso e o polimorfismo.

     A sua sintaxe é Classe A extends B, onde A é a subclasse (BAccount) e B é a superclasse (Account).

     É uma relação "é-um": uma conta para negócios (BAccount) é uma conta (Account).

     Generalização (Account) e Especialização (BAccount).

     Extensão de classe: adicionar novos comportamentos e atributos.

     Herança é uma assoicação entre classes (e não objetos).
    */
}

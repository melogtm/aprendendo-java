public class ClassesEMetodosFinal {
    /*
    Final é uma palavra reservada que pode ser usada em classes, métodos e variáveis.

    Quando uma classe é final, ela não pode ser herdada. Ex:
    final class SavingsAccount { ... } // Não pode ser herdada.

    Quando um método é final, ele não pode ser sobreposto. Ex:
    public final void withdraw(Double amount) { ... } // Não pode ser sobreposto.

    O string é uma classe final, ou seja, não pode ser herdada. Isso é melhor para performance e segurança
    de algumas operações.

    */    
}

public class TiposVariaveis {
    public static void main(String[] args) {
        // Tipos Numéricos Inteiros
        byte numByte = 127; // 8 bits, vai de -128 a 127
        short numShort = 32767; // 16 bits, vai de -32768 a 32767
        int numInt = 2147483647; // 32 bits, vai de -2147483648 a 2147483647
        long numLong = 9223372036854775807L; // 64 bits, vai de -9223372036854775808 a 9223372036854775807
        // Tipos numéricos com ponto fluante
        float numFloat = 3.4028235E38f; // 32 bits, vai de 1.4e-45 a 3.4028235e38
        double numDouble = 1.7976931348623157E308; // 64 bits, vai de 4.9e-324 a 1.7976931348623157e308
        // Tipo caractere
        char caractere = 'A'; // 16 bits, vai de 0 a 65535, ou de '\u0000' a '\uffff'
        // Tipo booleano
        boolean verdadeiro = true; // true ou false, com valor padrão false se não for inicializado
    }
}
/*
 Construtores - são métodos especiais que são chamados quando um objeto é instanciado.

 Usamos para inicializar os atributos do objeto. Permitir ou obrigar que os atributos sejam passados na instanciação.

 Se um construtor não for definido, a classe possui um construtor padrão (sem argumentos).

 É possível especificar mais de um construtor na classe (sobrecarga).
*/
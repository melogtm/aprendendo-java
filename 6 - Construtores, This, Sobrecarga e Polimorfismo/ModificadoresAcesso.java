/* 
 *  Modificadores de Acesso - public, private, protected 
 * 
 * private - Atributos e métodos privados só podem ser acessados dentro da própria classe.
 * 
 * public - Atributos e métodos públicos podem ser acessados por todas as classes do projeto, ao menos que ele resida em um módulo diferente que não exporte o pacote onde ele está.
 * 
 * // Esse aí tem relação com Herança
 * protected - Atributos e métodos protegidos só podem ser acessados por classes do mesmo pacote ou subclasses.
 * 
 * (nada) ou default - Atributos e métodos sem modificador de acesso só podem ser acessados por classes do mesmo pacote.
 * 
 */